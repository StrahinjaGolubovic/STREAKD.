// Helper function to get icon by name
export const getAchievementIcon = (iconName: string, props?: IconProps) => {
    const iconMap: Record<string, React.FC<IconProps>> = {
        target: TargetIcon,
        flame: FlameIcon,
        muscle: MuscleIcon,
        star: StarIcon,
        hundred: HundredIcon,
        camera: CameraIcon,
        video: VideoIcon,
        trophy: TrophyIcon,
        sparkles: SparklesIcon,
        diamond: DiamondIcon,
        medal_bronze: (p) => <MedalIcon {...p} variant="bronze" />,
        medal_silver: (p) => <MedalIcon {...p} variant="silver" />,
        medal_gold: (p) => <MedalIcon {...p} variant="gold" />,
        coins: CoinsIcon,
        hand_wave: HandWaveIcon,
        users: UsersIcon,
        handshake: HandshakeIcon,
        shield: ShieldIcon,
        bell: BellIcon,
        sunrise: SunriseIcon,
        moon: MoonIcon,
        dumbbell: DumbbellIcon,
        refresh: RefreshIcon,
        party: PartyIcon,
        lightbulb: LightbulbIcon,
        alert: AlertIcon,
        clock: ClockIcon,
        settings: SettingsIcon,
        iron: IronStrengthIcon,
    };

    const IconComponent = iconMap[iconName] || TargetIcon;
    return <IconComponent {...props} />;
};
